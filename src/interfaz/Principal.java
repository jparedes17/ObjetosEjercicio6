    private void cmbServirTazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbServirTazaActionPerformed
        // TODO add your handling code here
        int taza;
        if (txtCantidadTaza.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Ingrese la cantidad a servir", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else 
        {
        taza= Integer.parseInt(txtCantidadTaza.getText());
        
        c.ServirTaza(taza);
        
        txtCantidadActual.setText(""+c.getCantidad_Actual());
        
        txtCantidadTaza.setText("");
        }
    }//GEN-LAST:event_cmbServirTazaActionPerformed

