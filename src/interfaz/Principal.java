    private void cmbAgregarCafeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbAgregarCafeActionPerformed
        // TODO add your handling code here:
        int agregarcafe;
        if (txtCafeAgregar.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Ingrese la cantidad a Agregar", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else {
            
        agregarcafe= Integer.parseInt(txtCafeAgregar.getText());
        if (c.getCantidad_Actual()<agregarcafe)
        {
            JOptionPane.showMessageDialog(this, "Supera la capacidad maxima de la Cafetera", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else {
        c.AgregarCafe(agregarcafe);
        txtCantidadActual.setText(""+c.getCantidad_Actual());
        txtCafeAgregar.setText("");
        }
        }
        
    }//GEN-LAST:event_cmbAgregarCafeActionPerformed

